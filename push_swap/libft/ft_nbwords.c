int	ft_nbwords(char *s, char c)
{
	int i;
	int count;

	i = 0;
	count = 0;
	while (*s)
	{
		if (count == 0 && *s != c)
		{
			count = 1;
			i++;
		}
		else if (count == 1 && *s == c)
			count = 0;
		s++;
	}
	return (i);
}
